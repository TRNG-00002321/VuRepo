class Calculator:

    def add(self, a, b):
        return a + b

    def sub(self, a, b):
        return a - b

    def mul(self, a, b):
        return a * b

    def div(self, a, b):
        return a / b

    def is_even(self, a):
        return a % 2 == 0

    def double(self, a):
        return a * 2



